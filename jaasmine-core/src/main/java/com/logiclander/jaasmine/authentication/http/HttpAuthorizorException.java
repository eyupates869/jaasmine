package com.logiclander.jaasmine.authentication.http;

public class HttpAuthorizorException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8030242074489354961L;

	public HttpAuthorizorException() {
		super();
	}

	public HttpAuthorizorException(String message, Throwable cause) {
		super(message, cause);
	}

	public HttpAuthorizorException(String message) {
		super(message);
	}

	public HttpAuthorizorException(Throwable cause) {
		super(cause);
	}

}
